$(document).ready(function(){
  $("#contact_form").submit(function(e){
    e.preventDefault();
    var errors="";

    $(".required").each(function(){
      if($(this).val()==="")
      {
        errors += $(this).attr("id") + " is a required field!<br />";
        console.log($(this).attr("id"))
      }
    });

    if(!isValidEmailAddress($("#email").val()))
    {
      errors +="Please enter a valid email address!<br />";
    } 

    if($("#message").val()=="")
    {
      errors+= "A message is required<br />";
    }
    
    if(errors.length > 0)
    {
      $("#response").html(errors).slideDown().addClass("error").removeClass("success");
      return false;
    }
    
    var $email  = $.post( "http://chefliz.ca/m.php", $(this).serialize() );
    $email.done(function(data){
      $("#response").empty().append(data).removeClass("error").addClass("success").slideDown();
      $("#contact_form")[0].reset();
    })
  })
});
